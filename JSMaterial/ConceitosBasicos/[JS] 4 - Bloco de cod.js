// Definição de bloco de código
/* Basicamente tem a estrutura contida dentro de chaves {}
    Os blocos permite que voce realize o agrupamento de sentenças usando as chaves

    */

// 1 - Primeiro bloco de código

 {
    console.log('Primeiro bloco de código.');

 }

 {

    {
        console.log('Segundo bloco de código.')
    }
 }

 /*
 Lembrando que blocos podem possuir diversos níveis, estando um contio dentro do outro ou em algum subnível.
 */