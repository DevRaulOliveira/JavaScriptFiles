// Tipos de variáveis em Java Script

// 1 - Metodo VAR

var a = 3; //VAR - Palavra reservada a - Identificador
console.log(a); // Variávels a recebe o valor numérico 3 no terminal.

// 2- Metodo LET
let b = 4;

/* Diferenças entre VAR e LET
A variável VAR permite a "redeclaração de variável" no mesmo bloco enquanto LET impede que a variável seja utilizada novamente
retornando assim erro no terminal.
*/

// 3 - Atribuindo o valor diretamente a variável
a = 500;
b = 400;

// 4 - Metodo Const, impede a alteração do valor da variável ao longo de todo código.
const c = 100; // Ao longo de todo o código 'c' não terá seu valor alterado

/* É preciso entender o algoritmo para atribuir as propriedades de maneira correta.
O que é constante, é indicado utilizar const, já em outros casos Let
*/